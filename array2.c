/**
 * 二维数组
 */
#include <stdio.h>

int main()
{
	/* 一个带有5行2列的数组 */
	int a[5][2] = {
		{0,0},
		{1,2},
		{2,4},
		{3,6},
		{4,8}
	};
	int i,j;
	
	/* 访问数组中每个元素的值 */
	for (i=0;i<5;i++) {
		for (j=0;j<2;j++) {
			printf("a[%d][%d] = %d\n", i, j, a[i][j]);
		}
	}

	/* 二维数组存放字符串，读取时当一维数组使用 */
	char names[6][50] = {"马超", "关平", "赵云", "张飞", "关羽", "刘备"};
	for (i=0;i<6;i++) {
		printf("悍将名称: %s\n", names[i]);
	}

	return 0;
}
